<?php
// กำหนดจำนวนเงินของนายสมชาย (สามารถเปลี่ยนตัวเลขเพื่อทดสอบได้)
$money = 450; 

if ($money < 500) {
    echo "นายสมชาย ใจดี มีเงินน้อยกว่า 500 บาท: เขาจะสามารถซื้อกางเกงได้";
} 
elseif ($money < 1000) {
    echo "นายสมชาย ใจดี มีเงินน้อยกว่า 1000 บาท: เขาจะสามารถซื้อกางเกงและเสื้อได้";
} 
elseif ($money < 1500) {
    echo "นายสมชาย ใจดี มีเงินน้อยกว่า 1500 บาท: เขาจะสามารถซื้อกางเกง เสื้อ และรองเท้าได้";
} 
else {
    // เงื่อนไขเพิ่มเติมในกรณีที่มีเงินตั้งแต่ 1500 บาทขึ้นไป
    echo "นายสมชาย ใจดี มีเงินตั้งแต่ 1500 บาทขึ้นไป: เขาจะสามารถซื้อของได้ทุกอย่างและมีเงินเหลือ";
}
